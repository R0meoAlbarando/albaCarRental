    public boolean carInfoDuplicateCheck(){
        
        dbConnector dbc = new dbConnector();
        
        try{
            String sql = "SELECT * FROM tbl_car WHERE c_plateNo. = '"+plateNo.getText()+"'";
            ResultSet rs = dbc.getData(sql);
            
            if(rs.next()){
                plate = rs.getString("c_plateNo.");
                if(plate.equals(plateNo.getText())){
                    JOptionPane.showMessageDialog(null, "Plate Number is Already Used.");
                    plateNo.setText("");
                }
                return true;
            }else{
                return false;
            }
        }catch(SQLException ex){
            System.out.println(""+ex);
            return false;
        }
    }

