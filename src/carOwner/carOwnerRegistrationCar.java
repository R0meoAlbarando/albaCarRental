    private void addCarDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCarDataActionPerformed
        // Check if all required fields are filled
        if (brand.getText().isEmpty() || model.getText().isEmpty() || type.getText().isEmpty() || color.getText().isEmpty() || capacity.getText().isEmpty() || rate.getText().isEmpty() || plateNo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in all required data.");
        }else if(carInfoDuplicateCheck()){    
            System.out.println("Duplicated Exist");
        } else {
            // Retrieve data from GUI components 
            brand.getText();
            model.getText();
            type.getText();
            color.getText();
            capacity.getText();
            rate.getText();
            plateNo.getText();
        
            // Attempt to insert data into the database
            dbConnector dbc = new dbConnector();
        
            if (dbc.insertData("INSERT INTO tbl_car (c_brand, c_model, c_type, c_color, c_capacity, c_rate, c_plateNo.) VALUES ('" + brand + "', '" + model + "', '" + type + "', '" + color+ "', '" + capacity + "', '" + rate + "', '" + plateNo + "')")) {
                JOptionPane.showMessageDialog(null, "Car Information Inserted Successfully!");
                // Clear GUI fields after successful insertion
                brand.setText("");
                model.setText("");
                type.setText("");
                color.setText("");
                capacity.setText("");
                rate.setText("");
                plateNo.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Failed to insert car information. Connection error or invalid data.");
            }
        }
    }//GEN-LAST:event_addCarDataActionPerformed

